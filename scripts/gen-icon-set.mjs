import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

// Regenerate src/lib/iconSet.generated.ts: a STATIC map of just the icons the
// picker offers (the curated categories in src/lib/opIcons.ts), imported by name
// from @tabler/icons-react so the bundler tree-shakes to only those ~200 icons
// instead of pulling the whole ~6000-icon barrel (a 2.6 MB chunk). This shrinks
// the bundle, speeds first paint, and — because WebView2 code-caches compiled
// JS per file — drastically cuts the cache churn seen on install/uninstall.
//
//   node scripts/gen-icon-set.mjs
//
// The curated names in opIcons.ts are the single source of truth: browse, search
// and what gets bundled all derive from this one list. Add an icon there and
// rerun this + gen-icon-tags.mjs.
const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OP_ICONS = path.join(ROOT, "src", "lib", "opIcons.ts");
const VALID_SRC = path.join(
  ROOT,
  "node_modules",
  "@tabler",
  "icons-react",
  "dist",
  "esm",
  "dynamic-imports.mjs",
);
const OUT = path.join(ROOT, "src", "lib", "iconSet.generated.ts");

/** Curated icon names, in first-seen order, extracted from opIcons.ts. */
function curatedNames() {
  const src = readFileSync(OP_ICONS, "utf8");
  const seen = new Set();
  for (const m of src.matchAll(/"(Icon[A-Za-z0-9]+)"/g)) seen.add(m[1]);
  return [...seen];
}

/** Every real export name of @tabler/icons-react (from its dynamic-imports map,
 * whose targets are `./icons/IconName.ts`). Guards against a typo'd name in
 * opIcons.ts becoming a static import that fails the build. */
function validNames() {
  const src = readFileSync(VALID_SRC, "utf8");
  const set = new Set();
  for (const m of src.matchAll(/\/(Icon[A-Za-z0-9]+)\.ts/g)) set.add(m[1]);
  return set;
}

const valid = validNames();
const curated = curatedNames();
const good = curated.filter((n) => valid.has(n)).sort();
const bad = curated.filter((n) => !valid.has(n));
if (bad.length) {
  console.warn(
    `[icon-set] skipping ${bad.length} unknown name(s) from opIcons.ts: ${bad.join(", ")}`,
  );
}

const lines = [
  "// AUTO-GENERATED by scripts/gen-icon-set.mjs — do not edit.",
  "// The picker's icon set (curated in opIcons.ts), imported statically so the",
  "// bundler ships only these icons instead of the whole ~6000-icon barrel.",
  'import type { ComponentType } from "react";',
  "import {",
  ...good.map((n) => `  ${n},`),
  '} from "@tabler/icons-react";',
  "",
  "type IconComponent = ComponentType<{",
  "  size?: number | string;",
  "  className?: string;",
  "  color?: string;",
  "}>;",
  "",
  "/** name (e.g. \"IconBrush\") → component, for every icon the picker offers. */",
  "export const ICON_SET: Record<string, IconComponent> = {",
  ...good.map((n) => `  ${n},`),
  "};",
  "",
];
writeFileSync(OUT, lines.join("\n"));
console.log(
  `[icon-set] wrote ${good.length} icons → ${path.relative(ROOT, OUT)}`,
);
